package ch03;

public class Code0307{
    // char: 단일 문자를 나타내는 자료형
    public static void main(String[] args){
        char a1 = 'a'; // 문자로 표현
        char a2 = 97; // 아스키 코드로 표현
        char a3 = '\u0061'; // 유니코드로 표현
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
    }
}
